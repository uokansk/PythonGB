from string import ascii_letters


def delete_char(text: str) -> str:
    """
    >>> delete_char('hello py')
    'hello py'
    >>> delete_char('HeLLo Py')
    'hello py'
    >>> delete_char('hello! py,.')
    'hello py'
    >>> delete_char('hel4lo –ø—Ä–∏–≤–µ—Çpy')
    'hello py'
    >>> delete_char('HeLLo! –ø—Ä–∏–≤–µ—ÇPy–±,.')
    'hello py'
    """
    return ("".join(i for i in text if i in ascii_letters + ' ')).lower()


